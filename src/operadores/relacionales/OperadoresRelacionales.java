
package operadores.relacionales;

public class OperadoresRelacionales {

    public static void main(String[] args) {
        int a = 10, b = 5;
    
        // Operadores relacionales
        System.out.println("a == b: " + (a == b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a != b: " + (a != b));
    }
    
}
